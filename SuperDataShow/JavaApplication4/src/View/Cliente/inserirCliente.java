    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
	    String validacao = "Campos Obrigatórios";
	    int flag = 0;

	    if (jTextFieldNomeContato.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo Nome Contato";
		    flag = 1;
	    }
	    if (jTextFieldRazãoSocial.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo Razão Social";
		    flag = 1;
	    }

	    if (jFormattedTextFieldCNPJ.getText().equals("  .   .   /    -  ")) {
		    validacao = validacao + "\n * Preencha o campo CNPJ";
		    flag = 1;
	    }

	    if (jTextFieldCidade.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo Cidade";
		    flag = 1;
	    }

	    if (jTextFieldRua.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo Rua";
		    flag = 1;
	    }

	    if (jComboBoxEstado.getSelectedIndex() == 0) {
		    validacao = validacao + "\n * Escolha  o seu Estado";
		    flag = 1;
	    }
	    if (jTextFieldCep.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo CEP";
		    flag = 1;
	    }
	    if (jTextFieldEmail.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo E-mail";
		    flag = 1;
	    }
	    if (jTextFieldTelefone.getText().isEmpty()) {
		    validacao = validacao + "\n * Preencha o campo Telefone";
		    flag = 1;
	    }

	    if (flag == 1) {
		    JOptionPane.showMessageDialog(null, validacao);
	    } else {

		    //  Botão salvar
		    try {
			    //PACOTE METODOS
			    Connection con = new Conexao().AbrirConexao();
			    Cliente cliente = new Cliente();
			    ClienteController cc = new ClienteController(con);

			    //ATENÇÃO O EXEMPLO ABAIXO É PARA COMPONENTES JTEXTFIELD
			    cliente.setNomeContato(jTextFieldNomeContato.getText());
			    cliente.setRazaoSocial(jTextFieldRazãoSocial.getText());
			    cliente.setCnpj(jFormattedTextFieldCNPJ.getText());
			    cliente.setCidade(jTextFieldCidade.getText());
			    cliente.setRua(jTextFieldRua.getText());
			    cliente.setEstado(jComboBoxEstado.getSelectedIndex() + "");
			    cliente.setCep(jTextFieldCep.getText());
			    cliente.setEmail(jTextFieldEmail.getText());
			    cliente.setTelefone(jTextFieldTelefone.getText());

			    cc.inserirCliente(cliente);

			    JOptionPane.showMessageDialog(null, "Salvo com sucesso");
		    } catch (Exception ex) {
			    JOptionPane.showMessageDialog(null, "Erro:\n" + ex.getMessage());
		    }

	    }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

