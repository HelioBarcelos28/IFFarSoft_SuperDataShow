      private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed

            Connection con = new Conexao().AbrirConexao();
            ProjetorController pc = new ProjetorController(con);

		try {

			int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este cadastro?", "Confirmação", JOptionPane.YES_NO_OPTION);//criar uma tela para perguntar se quer ou não excluir
			if (resposta == JOptionPane.YES_NO_OPTION) {

				pc.excluirProjetor(jTextField1.getText());
				JOptionPane.showMessageDialog(null, "Projetor excluido com sucesso!");
				limparTabela(evt);
			}

		} catch (HeadlessException ex) {
			JOptionPane.showMessageDialog(null, "Erro:\n" + ex.getMessage());
		}
      }//GEN-LAST:event_botaoExcluirActionPerformed

