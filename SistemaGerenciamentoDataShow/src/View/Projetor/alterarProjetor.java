      private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed

		AbstractAction acaoEnter = new AbstractAction() {

			@Override
			public void actionPerformed(ActionEvent e) {

				try {

					Connection con = new Conexao().AbrirConexao();
					ProjetorController pc = new ProjetorController(con);
					List<Projetor> projetor = null;

					switch (jComboBox1.getSelectedItem().toString()) {

						case "":
						case "ID":

							projetor = pc.listarTodosProjetores();
							break;
						case "Marca":

							projetor = pc.listarProjetoresEspecificosMarca(jTextField2.getText(), 0);
							break;
						case "Nº de Série":

							projetor = pc.listarProjetoresEspecificosNumeroSerie(jTextField2.getText(), 0);
							break;
						case "Comprado em":

							projetor = pc.listarProjetoresEspecificosDataCompra(jTextField2.getText(), 0);
							break;
						case "Ansi Lumens":

							projetor = pc.listarProjetoresEspecificosAnsiLumens(jTextField2.getText(), 0);
							break;
						case "Estado":

							projetor = pc.listarProjetoresEspecificosEstado(jTextField2.getText(), 0);
							break;
						case "Preço":

							projetor = pc.listarProjetoresEspecificosPreco(jTextField2.getText(), 0);
							break;
						case "Hrs restantes":

							projetor = pc.listarProjetoresEspecificosNumeroHorasUso(jTextField2.getText(), 0);
							break;
					}

					if (projetor.isEmpty()) {

						JOptionPane.showMessageDialog(null, "Não existem dados para essa busca");
					} else {

						DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
						for (int i = dtm.getRowCount(); i > 0; i--) {

							dtm.removeRow(i - 1);
						}

						for (int i = 0; i < projetor.size(); i++) {

							dtm.addRow(new Object[]{
								projetor.get(i).getIdProjetor(),
								projetor.get(i).getMarca(),
								projetor.get(i).getModelo(),
								projetor.get(i).getNumeroSerie(),
								projetor.get(i).getDataCompra(),
								projetor.get(i).getAnsiLumens(),
								projetor.get(i).getEstado(),
								projetor.get(i).getPreco(),
								projetor.get(i).getNumeroSerie(),});
						}

						jTextField2.setText("");
					}

				} catch (HeadlessException ex) {

					JOptionPane.showMessageDialog(null, ex.getMessage());
				}
			}
		};

		AbstractAction acaoF1 = new AbstractAction() {

			@Override
			public void actionPerformed(ActionEvent e) {

				jComboBox1.setVisible(true);
				jTextField2.setVisible(false);
				jSeparator2.setVisible(false);
			}
		};

		JTextField enter = jTextField2;
		enter.getInputMap().put(KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0), "acaoEnter");
		enter.getActionMap().put("acaoEnter", acaoEnter);

		JTextField f1 = jTextField2;
		f1.getInputMap().put(KeyStroke.getKeyStroke(KeyEvent.VK_F1, 0), "acaoF1");
		f1.getActionMap().put("acaoF1", acaoF1);
      }//GEN-LAST:event_jTextField2KeyPressed

      private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

		int[] linha = jTable2.getSelectedRows();

		if (linha.length == 1) {

			try {

				idProjetor = (jTable2.getValueAt(linha[0], 0) + "");
				textFieldMarca.setText(jTable2.getValueAt(linha[0], 1) + "");
				textFieldModelo.setText(jTable2.getValueAt(linha[0], 2) + "");
				textFieldNumeroSerie.setText(jTable2.getValueAt(linha[0], 3) + "");
				textFieldDataCompra.setText(jTable2.getValueAt(linha[0], 4) + "");
				textFieldAnsiLumens.setText(jTable2.getValueAt(linha[0], 5) + "");
				textFieldPreco.setText(jTable2.getValueAt(linha[0], 7) + "");
			} catch (Exception e) {
				JOptionPane.showMessageDialog(null, "Erro ao popular tabela! " + e.getMessage());
			}
		}
      }//GEN-LAST:event_jTable2MouseClicked

      private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed

		Projetor projetor = new Projetor();
		Connection con = new Conexao().AbrirConexao();
		ProjetorController pc = new ProjetorController(con);

		boolean campoVazio = (textFieldAnsiLumens.getText().isEmpty()
			  ? (textFieldDataCompra.getText().isEmpty()
			  ? (textFieldMarca.getText().isEmpty()
			  ? (textFieldModelo.getText().isEmpty()
			  ? (textFieldNumeroSerie.getText().isEmpty()
			  ? (textFieldPreco.getText().isEmpty()) : false) : false) : false) : false) : false);

		if (campoVazio) {

			JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
		} else {

			projetor.setMarca(textFieldMarca.getText());
			projetor.setModelo(textFieldModelo.getText());
			projetor.setNumeroSerie(textFieldNumeroSerie.getText());
			projetor.setDataCompra(textFieldDataCompra.getText());
			projetor.setAnsiLumens(textFieldAnsiLumens.getText());

			switch (comboBoxEstado.getSelectedIndex()) {

				case 1:

					projetor.setEstado("D");
					break;
				case 2:

					projetor.setEstado("L");
					break;
				case 3:

					projetor.setEstado("M");
					break;
				case 4:

					projetor.setEstado("FE");
					break;
			}

			projetor.setPreco(textFieldPreco.getText());
			projetor.setNumeroHorasUso("100");

			try {

				pc.alterarProjetor(projetor, idProjetor);
				JOptionPane.showMessageDialog(null, "Projetor alterado com sucesso!");
			} catch (HeadlessException e) {
				System.out.println("deu merda");
			}
		}
      }//GEN-LAST:event_botaoAlterarActionPerformed

