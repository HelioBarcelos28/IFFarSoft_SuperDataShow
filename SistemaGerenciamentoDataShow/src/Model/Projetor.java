public class Projetor {

	private String idProjetor;
	private String marca;
	private String modelo;
	private String numeroSerie;
	private String dataCompra;
	private String ansiLumens;
	private String estado;
	private String preco;
	private String numeroHorasUso;

	public String getIdProjetor() {
		return idProjetor;
	}

	public void setIdProjetor(String idProjetor) {
		this.idProjetor = idProjetor;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

	public String getModelo() {
		return modelo;
	}

	public void setModelo(String modelo) {
		this.modelo = modelo;
	}

	public String getNumeroSerie() {
		return numeroSerie;
	}

	public void setNumeroSerie(String numeroSerie) {
		this.numeroSerie = numeroSerie;
	}

	public String getDataCompra() {
		return dataCompra;
	}

	public void setDataCompra(String dataCompra) {
		this.dataCompra = dataCompra;
	}

	public String getAnsiLumens() {
		return ansiLumens;
	}

	public void setAnsiLumens(String ansiLumens) {
		this.ansiLumens = ansiLumens;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getPreco() {
		return preco;
	}

	public void setPreco(String preco) {
		this.preco = preco;
	}

	public String getNumeroHorasUso() {
		return numeroHorasUso;
	}

	public void setNumeroHorasUso(String numeroHorasUso) {
		this.numeroHorasUso = numeroHorasUso;
	}
	
	
}
