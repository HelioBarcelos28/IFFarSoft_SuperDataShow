package Model;

/**
 *
 * @author Helio
 */
public class Projetores {
    private int idProjetores ;
    private String  marca ;
    private String  modelo ;
    private String  numSerie ;
    private String dataCompra;
    private String dataTrocaLampada;
    private String ansilumens ;
    private String estado;
    private Double preco;

    public int getIdProjetores() {
        return idProjetores;
    }

    public void setIdProjetores(int idProjetores) {
        this.idProjetores = idProjetores;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getNumSerie() {
        return numSerie;
    }

    public void setNumSerie(String numSerie) {
        this.numSerie = numSerie;
    }

    public String getDataCompra() {
        return dataCompra;
    }

    public void setDataCompra(String dataCompra) {
        this.dataCompra = dataCompra;
    }

    public String getDataTrocaLampada() {
        return dataTrocaLampada;
    }

    public void setDataTrocaLampada(String dataTrocaLampada) {
        this.dataTrocaLampada = dataTrocaLampada;
    }

    public String getAnsilumens() {
        return ansilumens;
    }

    public void setAnsilumens(String ansilumens) {
        this.ansilumens = ansilumens;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public Double getPreco() {
        return preco;
    }

    public void setPreco(Double preco) {
        this.preco = preco;
    }
    
    
    
    
   
}
