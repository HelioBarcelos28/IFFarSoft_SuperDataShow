private void jTextFieldSalarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldSalarioFocusLost
if(!jTextFieldSalario.getText().contains(".")){
jTextFieldSalario.setText(jTextFieldSalario.getText()+".00");
}

if(jTextFieldSalario.getText().substring(0,1).equals(".")){
jTextFieldSalario.setText("0"+jTextFieldSalario.getText());
}

if (jTextFieldSalario.getText().charAt(jTextFieldSalario.getText().length()-1)=='.'){
jTextFieldSalario.setText(jTextFieldSalario.getText()+"0");
}
}//GEN-LAST:event_jTextFieldSalarioFocusLost

private void jTextFieldSalarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSalarioKeyTyped
String caracteres="0123456789.,";

if(!caracteres.contains(evt.getKeyChar()+"")){
    evt.consume();
}

if(",".equals(evt.getKeyChar()+"")){
    evt.setKeyChar('.');
}

if((jTextFieldSalario.getText().contains("."))
        &&(".".equals(evt.getKeyChar()+""))) {
    evt.consume();
}


    if (jTextFieldSalario.getText().length() >= 5) {
        evt.consume();
    }
}//GEN-LAST:event_jTextFieldSalarioKeyTyped

