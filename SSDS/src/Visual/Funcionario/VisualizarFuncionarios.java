private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
  try {
        funcionariosdados   X = new funcionariosdados();
      
        List<funcionarios> funcionarios = X.getLista("%" + jTextFieldBusca.getText() + "%");
      
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = dtm.getRowCount(); i > 0; i--) {
            dtm.removeRow(i - 1);
        }
        if (funcionarios.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não existem dados para essa busca");
        }
        for (int i = 0; i < funcionarios.size(); i++) {
            dtm.addRow(new Object[]{
                        funcionarios.get(i).getIdfunionarios(),
                       funcionarios.get(i).getNome(),
                        funcionarios.get(i).getSetor(),
                        funcionarios.get(i).getCpf(),
                        funcionarios.get(i).getSexo(),
                        funcionarios.get(i).getDatanasc(),
                        funcionarios.get(i).getCidade(),
                       funcionarios.get(i).getEndereco(),
                       funcionarios.get(i).getTelefone(),
                       funcionarios.get(i).getSalario(),
                        //COPIE E COLE O COMANDO ACIMA PARA CADA CAMPO
            });
        }
   }catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
    }
    // TODO add your handling code here:
}//GEN-LAST:event_jButtonPesquisarActionPerformed

