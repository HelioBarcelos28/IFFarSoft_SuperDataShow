    try {
            JasperPrint rel = null;
            String arquivoJasper = "relatorios/servicosindividual/servico.jasper";
            Map parametros = new HashMap();
            parametros.clear();
//LINHA ABAIXO PARA ESCOLHER O REGISTRO A SER IMPRESSO
            parametros.put("cod", jTextFieldIdservico.getText());
 
            rel = JasperFillManager.fillReport(arquivoJasper, parametros, CriaConexao.getConexao());
             JasperPrintManager.printReport(rel, true);//linha para impressao
          
     } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Erro com arquivo do jasperReport,\n se persistir reinstale o sistema\n" + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,//msg de erro no sql
                    "Ocorreu um erro!\n" + e.getMessage(),
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        }// TODO add your handling code here:
