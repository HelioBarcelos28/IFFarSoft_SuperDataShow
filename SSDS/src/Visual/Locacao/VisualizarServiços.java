private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
   try {
        servicosdados   X = new servicosdados();
      
        List<servicos> servicos = X.getLista("%" + jTextFieldBusca.getText() + "%");
      
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = dtm.getRowCount(); i > 0; i--) {
            dtm.removeRow(i - 1);
        }
        if (servicos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não existem dados para essa busca");
        }
        for (int i = 0; i < servicos.size(); i++) {
            dtm.addRow(new Object[]{
                        servicos.get(i).getIdservicos(),
                        servicos.get(i).getNomecliente(),
                        servicos.get(i).getTiposervico(),
                        servicos.get(i).getFuncionario(),
                        servicos.get(i).getSetor(),
                        servicos.get(i).getValorunitario(),
                        servicos.get(i).getQuantidade(),
                        servicos.get(i).getValortotal(),
                        servicos.get(i).getDataservico(),
                        //COPIE E COLE O COMANDO ACIMA PARA CADA CAMPO
            });
        }
   }catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
    }
}//GEN-LAST:event_jButtonPesquisarActionPerformed

